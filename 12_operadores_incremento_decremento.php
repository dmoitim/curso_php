<?php
$valor = 20;

// Pré-Incremento
echo "Pré-incremento: " . ++$valor;
echo "<hr>";

// Pós-Incremento
echo "Pós-incremento, continuou com: " . $valor++;
echo "<br>";
echo "Imprimindo valor novamente: " . $valor;
echo "<hr>";

$valor = 20;

// Pré-Decremento
echo "Pré-decremento: " . --$valor;
echo "<hr>";

// Pós-Decremento
echo "Pós-dencremento, continuou com: " . $valor--;
echo "<br>";
echo "Imprimindo valor novamente: " . $valor;
echo "<hr>";
